CREATE OR REPLACE PROCEDURE UPDATE_PRODUCT
(
		PID				IN		INT,
		PNAME 		IN 		VARCHAR,
		PUNIT 		IN 		VARCHAR,
		PSTOCK 		IN 		INT,
		PRICE 		IN 		INT,
		PCAT 			IN 		VARCHAR,
		PSUBCAT 	IN 		VARCHAR
)
IS
BEGIN

		UPDATE 
				PRODUCT
		SET
				PRODUCT_NAME = PNAME,
				UNIT = PUNIT,
				UNITS_IN_STOCK = PSTOCK,
				PRICE_PER_UNIT = PRICE,
				CATEGORY = PCAT,
				SUB_CATEGORY = PSUBCAT
		WHERE
				PRODUCT_ID = PID;

END;
/



