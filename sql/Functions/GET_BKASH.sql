CREATE OR REPLACE FUNCTION GET_BKASH
(
	MY_ID 	IN 	INT,
	ATT		IN	VARCHAR
) 
RETURN VARCHAR IS

	RET 	VARCHAR(100);

BEGIN

	IF 		ATT = 'PHONE_NO'	THEN
		SELECT BKASH_PHONE_NO INTO RET FROM CUSTOMER_PAYMENT_INFO WHERE CUSTOMER_ID = MY_ID;
	END IF;
	
	RETURN RET;

EXCEPTION

	WHEN NO_DATA_FOUND THEN
		RETURN '';
	WHEN OTHERS THEN
		RETURN '';

END;
/